
def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):        # integer division
    return x // y

def mod(x, y):
    return x % y


# Logical operations
def and_func(x, y):
    return x and y

def or_func(x, y):
    return x or y

def not_func(x):
    return not x


# Comparison operations
def greater_than(x, y):
    return x > y

def less_equal(x, y):
    return x <= y

def equal(x, y):
    return x == y
